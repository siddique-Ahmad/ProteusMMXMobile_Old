using Xamarin.Forms;
using Xamarin.Forms.Xaml;

namespace ProteusMMX.Views.ClosedWorkorder
{
	[XamlCompilation(XamlCompilationOptions.Compile)]
	public partial class ClosedWorkorderStockroomPartsForIOS : ContentPage
	{
		public ClosedWorkorderStockroomPartsForIOS ()
		{
			InitializeComponent ();
            ((NavigationPage)Application.Current.MainPage).BarBackgroundColor = Color.FromHex("#006de0");
            ((NavigationPage)Application.Current.MainPage).BarTextColor = Color.White;
        }
        public ViewModel.ClosedWorkorder.ClosedWorkorderStockroomPartsViewModelForIOS ViewModel
        {
            get
            {
                return this.BindingContext as ViewModel.ClosedWorkorder.ClosedWorkorderStockroomPartsViewModelForIOS;
            }
        }
        protected override async void OnAppearing()
        {
            base.OnAppearing();

            if (BindingContext is IHandleViewAppearing viewAware)
            {
                ((NavigationPage)Application.Current.MainPage).BarBackgroundColor = Color.FromHex("#006de0");
                ((NavigationPage)Application.Current.MainPage).BarTextColor = Color.White;
                await viewAware.OnViewAppearingAsync(this);
            }
        }

        protected override async void OnDisappearing()
        {
            base.OnDisappearing();

            if (BindingContext is IHandleViewDisappearing viewAware)
            {
                await viewAware.OnViewDisappearingAsync(this);
            }
        }
    }
